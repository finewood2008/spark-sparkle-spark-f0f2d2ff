export interface QuickAction {
  label: string;
  value: string;
  icon?: string;
  variant?: 'default' | 'primary' | 'outline';
}

export interface OptionCard {
  id: string;
  title: string;
  description?: string;
  image?: string;
  selected?: boolean;
}

export interface ChoiceOption {
  id: string;
  label: string;
  emoji?: string;
  /** Short one-line description shown under the label in card-style choices. */
  description?: string;
  /** Visual style hint — 'card' renders an icon+title+desc card, undefined falls back to pill. */
  variant?: 'pill' | 'card';
  /** When set, clicking this choice triggers the clarify flow with this angle prompt
   *  appended to the original user prompt for generation. */
  anglePrompt?: string;
  /** Original user prompt this choice is clarifying for (used by clarify flow). */
  clarifyForPrompt?: string;
}

/** Result of analyze-intent edge function — drives the "smart pre-generation" flow */
export interface IntentAnalysis {
  intentType: string;
  matchedAssets: string[];
  matchedRules: string[];
  risks: string[];
  clarifyQuestion: {
    question: string;
    options: Array<{ id: string; label: string; anglePrompt: string; emoji?: string; description?: string }>;
  } | null;
  skipClarify: boolean;
}

export interface ReviewTaskData {
  source: 'schedule' | 'auto' | 'manual';
  taskName: string;
  triggeredAt: string;
  topic?: string;
}

export interface ChatMessage {
  id: string;
  role: 'user' | 'assistant';
  content: string;
  timestamp: string;
  suggestions?: string[];
  actions?: QuickAction[];
  options?: OptionCard[];
  choices?: ChoiceOption[];
  image?: string;
  contentItem?: ContentItem;
  reportData?: Record<string, unknown>;
  reviewTask?: ReviewTaskData;
  /** Lightweight reminder pointing to /review (replaces full ReviewCard in chat) */
  reviewReminder?: {
    taskName?: string;
    message?: string;
    item?: Pick<ContentItem, 'id' | 'title' | 'content' | 'status'>;
  };
  distribution?: DistributionData;
  scheduleCard?: ScheduleCardData;
  metricsCard?: MetricsCardData;
  /** When set, renders an error bubble with a retry action */
  error?: {
    message: string;
    /** Original user prompt to retry; if absent, retry button is hidden */
    retryPrompt?: string;
    /** Which handler to invoke on retry */
    retryMode?: 'chat' | 'generate';
  };
}

export interface MetricsCardData {
  reviewItemId: string;
  title: string;
  platform: string; // 'all' | Platform
  publishedPlatforms: string[];
  fetchedAt: string;
  metrics: {
    views: number;
    likes: number;
    comments: number;
    saves: number;
    shares: number;
  };
  aiInsight: string;
  source: 'mock' | 'real';
}

export type ContentStatus = 'draft' | 'reviewing' | 'approved' | 'published' | 'rejected';
export type Platform = 'xiaohongshu' | 'wechat' | 'douyin' | 'tiktok' | 'instagram';

export interface DistributionData {
  contentId: string;
  title: string;
  defaultPlatforms?: Platform[];
  /** Platforms it has been published to (sets card to success state on restore) */
  publishedPlatforms?: Platform[];
}

export interface ScheduleCardData {
  /** ID for tracking the proposed plan */
  id: string;
  /** Suggested topic parsed from user message */
  suggestedTopic?: string;
  /** Suggested frequency parsed from user message */
  suggestedFrequency?: 'daily' | 'weekly';
  /** Whether the plan was already confirmed (for restore) */
  confirmed?: boolean;
}

export interface ContentVersion {
  id: string;
  title: string;
  content: string;
  cta?: string;
  tags?: string[];
  coverImage?: string;
  savedAt: string;
  label?: string;
}

export interface ContentItem {
  id: string;
  title: string;
  content: string;
  platform: Platform;
  status: ContentStatus;
  coverImage?: string;
  tags?: string[];
  cta?: string;
  createdAt: string;
  updatedAt: string;
  publishedAt?: string;
  metrics?: ContentMetrics;
  autoGenerated?: boolean;
  versions?: ContentVersion[];
  /** AI's explanation of creative choices (why this title / structure / CTA).
   *  Populated when generated via the new analyze-intent → generate flow. */
  reasoning?: string[];
  /** Pre-creation dialogue transcript that led to this article (creative-dialogue flow).
   *  Rendered as a collapsible "我们一起聊了 N 轮" section under the card. */
  dialogueHistory?: Array<{ role: 'user' | 'assistant'; content: string }>;
  /** Number of dialogue rounds (assistant turns) that produced this article. */
  dialogueTurns?: number;
}

export interface ContentMetrics {
  views: number;
  likes: number;
  comments: number;
  shares: number;
  saves?: number;
}

export interface ScheduleConfig {
  enabled: boolean;
  frequency: 'daily' | 'weekly' | 'custom';
  daysOfWeek: number[];
  platforms: Platform[];
  topics: string[];
  style: string;
  postsPerDay: number;
  scheduledTimes: string[];
  nextRunAt?: string;
}

